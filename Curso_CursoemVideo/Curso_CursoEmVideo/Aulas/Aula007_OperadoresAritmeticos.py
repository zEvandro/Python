# Operações Aritméticas:
# OPERADORES

# "+" Adição
# "-" Subtração
# "*" Multiplicação
# "/" Divisão
# "**" Potênciação
# "//" Divisão Inteira
# "%" Modulo ou Resto da Divisão

# ADIÇÃO
# 5 + 5 == 10

# SUBTRAÇÃO
# 5 - 2 == 3

# MULTIPLICAÇÃO
# 5 * 2 == 10

# DIVISÃO
# 5 / 2 == 2.5

# POTÊNCIA
# 5 ** 2 == 25(5*5)

# DIVISÃO INTEIRA
# 5 // 2 == 2 (Só mostra o resultado inteiro da divisão)

# MODULO ou RESTO DA DIVISÃO
# 5 % 2 == 1 (Só mostra o que sobrou da divisão)

# ORDEM DE PRECEDÊNCIA:

# 1º PARÊNTESES:()
# 2º POTÊNCIAS: **
# 3º MULTIPLICAÇÃO, DIVISÃO, DIVISÃO INTEIRA E RESTO DA DIVISÃO: * , / , // , %
# 4º SOMA e SUBTRAÇÃO: + , -




